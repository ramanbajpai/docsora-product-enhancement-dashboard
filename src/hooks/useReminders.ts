export function useReminders() {
  const [reminders, setReminders] = useState<Reminder[]>(initialReminders);
  const [showCompleted, setShowCompleted] = useState(false);
  const [smartSuggestions, setSmartSuggestions] = useState<SmartSuggestion[]>(mockSmartSuggestions);

  const pendingReminders = reminders.filter(r => !r.completed);
  const completedReminders = reminders.filter(r => r.completed);

  const addReminder = useCallback((data: NewReminderData) => {
    const newReminder: Reminder = {
      id: `user-${Date.now()}`,
      type: data.type,
      text: data.note || (data.linkedDocument ? typeToText[data.type](data.linkedDocument) : typeToText[data.type]("Document")),
      documentName: data.linkedDocument || "Unlinked",
      documentType: "sign", // Default, would be determined by actual document lookup
      dueDate: data.dueDate,
      dueTime: data.dueTime || "09:00",
      completed: false,
      isSystemGenerated: false,
      note: data.note || undefined,
      notificationMethod: data.notificationMethod,
    };
    setReminders(prev => [newReminder, ...prev]);
  }, []);

  const updateReminder = useCallback((id: string, data: Partial<NewReminderData>) => {
    setReminders(prev =>
      prev.map(r => {
        if (r.id !== id) return r;
        
        const updatedText = data.note || 
          (data.linkedDocument ? typeToText[data.type || r.type](data.linkedDocument) : r.text);
        
        return {
          ...r,
          type: data.type ?? r.type,
          text: updatedText,
          documentName: data.linkedDocument ?? r.documentName,
          dueDate: data.dueDate !== undefined ? data.dueDate : r.dueDate,
          dueTime: data.dueTime ?? r.dueTime,
          note: data.note ?? r.note,
          notificationMethod: data.notificationMethod ?? r.notificationMethod,
        };
      })
    );
  }, []);

  const completeReminder = useCallback((id: string) => {
    setReminders(prev => 
      prev.map(r => 
        r.id === id 
          ? { ...r, completed: true, completedAt: new Date() }
          : r
      )
    );
  }, []);

  const deleteReminder = useCallback((id: string) => {
    setReminders(prev => prev.filter(r => r.id !== id));
  }, []);

  const dismissSuggestion = useCallback((id: string) => {
    setSmartSuggestions(prev => prev.filter(s => s.id !== id));
  }, []);

  const acceptSuggestion = useCallback((suggestion: SmartSuggestion) => {
    const newReminder: Reminder = {
      id: `from-sug-${Date.now()}`,
      type: suggestion.type,
      text: suggestion.text,
      documentName: suggestion.documentName,
      documentType: suggestion.documentType,
      dueDate: suggestion.suggestedDate,
      dueTime: "09:00",
      completed: false,
      isSystemGenerated: false,
      notificationMethod: "in-app",
    };
    setReminders(prev => [newReminder, ...prev]);
    setSmartSuggestions(prev => prev.filter(s => s.id !== suggestion.id));
  }, []);

  const formatDueDate = useCallback((date: Date | null): string | null => {
    if (!date) return null;
    if (isToday(date)) return "Today";
    if (isTomorrow(date)) return "Tomorrow";
    return format(date, "MMM d");
  }, []);

  const formatDueDateTime = useCallback((date: Date | null, time: string): string | null => {
    if (!date) return null;
    const dateStr = isToday(date) ? "Today" : isTomorrow(date) ? "Tomorrow" : format(date, "MMM d");
    return `${dateStr} at ${time}`;
  }, []);

  const getNavigationPath = useCallback((reminder: Reminder): string => {
    switch (reminder.documentType) {
      case "sign":
        return `/track?tab=sign&document=${encodeURIComponent(reminder.documentName)}`;
      case "transfer":
        return `/transfer?document=${encodeURIComponent(reminder.documentName)}`;
      case "ai-check":
        return `/ai-check?document=${encodeURIComponent(reminder.documentName)}`;
      case "track":
        return `/track?document=${encodeURIComponent(reminder.documentName)}`;
      default:
        return "/";
    }
  }, []);

  return {
    reminders,
    pendingReminders,
    completedReminders,
    smartSuggestions,
    showCompleted,
    setShowCompleted,
    addReminder,
    updateReminder,
    completeReminder,
    deleteReminder,
    dismissSuggestion,
    acceptSuggestion,
    formatDueDate,
    formatDueDateTime,
    getNavigationPath,
  };
}