import { useState, useCallback } from "react";
import { format, addDays, isToday, isTomorrow } from "date-fns";

export type ReminderType = 
  | "follow-up-signature" 
  | "no-activity"
  | "deadline-approaching"
  | "resend-transfer" 
  | "review-ai-suggestions"
  | "send-reminder" 
  | "custom";

export type NotificationMethod = "in-app" | "email" | "both";

export interface Reminder {
  id: string;
  type: ReminderType;
  text: string;
  documentName: string;
  documentType: "sign" | "transfer" | "ai-check" | "track";
  dueDate: Date | null;
  dueTime: string; // "09:00" format
  completed: boolean;
  completedAt?: Date;
  isSystemGenerated: boolean;
  note?: string;
  notificationMethod: NotificationMethod;
}

export interface NewReminderData {
  type: ReminderType;
  linkedDocument: string | null;
  dueDate: Date | null;
  dueTime: string;
  note: string;
  notificationMethod: NotificationMethod;
}

export interface SmartSuggestion {
  id: string;
  type: ReminderType;
  text: string;
  documentName: string;
  documentType: "sign" | "transfer" | "ai-check" | "track";
  reason: string;
  suggestedDate: Date | null;
}

const typeToText: Record<ReminderType, (docName: string) => string> = {
  "follow-up-signature": (doc) => `Follow up on signature for ${doc}`,
  "no-activity": (doc) => `No activity on ${doc}`,
  "deadline-approaching": (doc) => `Deadline approaching for ${doc}`,
  "resend-transfer": (doc) => `Resend transfer link for ${doc}`,
  "review-ai-suggestions": (doc) => `Review AI suggestions for ${doc}`,
  "send-reminder": (doc) => `Send reminder for ${doc}`,
  "custom": () => "Custom reminder",
};

